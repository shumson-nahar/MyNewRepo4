package arrays;

public class One_Dimentional_Array {

	public static void main(String[] args) {
		int i []=new int[4];
		i[0]=10;
		i[1]=20;
		i[2]=30;
		i[3]=40;
		System.out.println("the size of this array is:"+i.length);
		for(int j= i.length-1;j>0;j--) {
			System.out.println(i[j]+"");
				
			
		}
		

	}

}
